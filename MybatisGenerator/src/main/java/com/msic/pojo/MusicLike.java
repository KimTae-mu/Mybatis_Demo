package com.msic.pojo;

public class MusicLike {
    private Integer msicId;

    private Float popular;

    private Float ballad;

    private Float rock;

    private Float folk;

    private Float electronica;

    private Float dance;

    private Float light;

    private Float classical;

    public Integer getMsicId() {
        return msicId;
    }

    public void setMsicId(Integer msicId) {
        this.msicId = msicId;
    }

    public Float getPopular() {
        return popular;
    }

    public void setPopular(Float popular) {
        this.popular = popular;
    }

    public Float getBallad() {
        return ballad;
    }

    public void setBallad(Float ballad) {
        this.ballad = ballad;
    }

    public Float getRock() {
        return rock;
    }

    public void setRock(Float rock) {
        this.rock = rock;
    }

    public Float getFolk() {
        return folk;
    }

    public void setFolk(Float folk) {
        this.folk = folk;
    }

    public Float getElectronica() {
        return electronica;
    }

    public void setElectronica(Float electronica) {
        this.electronica = electronica;
    }

    public Float getDance() {
        return dance;
    }

    public void setDance(Float dance) {
        this.dance = dance;
    }

    public Float getLight() {
        return light;
    }

    public void setLight(Float light) {
        this.light = light;
    }

    public Float getClassical() {
        return classical;
    }

    public void setClassical(Float classical) {
        this.classical = classical;
    }
}